"""A module that adds numbers together"""

def add_numbers(num_1, num_2):
    """Add number together


    Parameters
    ----------------

    Args:
        num_1 (int):
            the first number to add
        num_2 (int): 
            the second number to add 

    Return 
    ----------------

        the sum of two numbers 
    """

    return num_1 + num_2
